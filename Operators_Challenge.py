# Python Identity Operators
x = ["mango", "banana"]
y = ["mango", "banana"]
z = x
print(x is z)
True
print(x is y)
False
print(x == y)
True
# Python Membership Operators
x = ["mango", "banana"]
y = ["mango"]
print ("mango" in x)
True
print ("banana" not in y)
True
# Bitwise Operators
a = 10
b = 4
print("a & b =", a & b)
a & b == 0
print("a | b =", a | b)
a | b == 14
